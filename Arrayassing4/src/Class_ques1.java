public class Class_ques1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 3 ;
        int c = a ;// c = 5
        int d = b ;// d = 3
        b  = c ;// b = 5 ;
        a = d ;// a = 3 ;
        System.out.println( "value of a is :" +a);
        System.out.println( "value of b is :" + b);

    }
}
